package project.uprising.Challenge;

public class Exercise {
    private int exerciseId;
    private int exerciseImageId;
    private String targetMuscles;
    private String goal;
    private String experience;
    private String gender;
    private String requiredEquipments;
    private String exerciseTitle;
    private String repRange;
    private String weightRange;

    // Getters and Setters
    public int getExerciseId() { return exerciseId; }
    public void setExerciseId(int exerciseId) { this.exerciseId = exerciseId; }

    public int getExerciseImageId() { return exerciseImageId; }
    public void setExerciseImageId(int exerciseImageId) { this.exerciseImageId = exerciseImageId; }

    public String getTargetMuscles() { return targetMuscles; }
    public void setTargetMuscles(String targetMuscles) { this.targetMuscles = targetMuscles; }

    public String getGoal() { return goal; }
    public void setGoal(String goal) { this.goal = goal; }

    public String getExperience() { return experience; }
    public void setExperience(String experience) { this.experience = experience; }

    public String getGender() { return gender; }
    public void setGender(String gender) { this.gender = gender; }

    public String getRequiredEquipments() { return requiredEquipments; }
    public void setRequiredEquipments(String requiredEquipments) { this.requiredEquipments = requiredEquipments; }

    public String getExerciseTitle() { return exerciseTitle; }
    public void setExerciseTitle(String exerciseTitle) { this.exerciseTitle = exerciseTitle; }

    public String getRepRange() { return repRange; }
    public void setRepRange(String repRange) { this.repRange = repRange; }

    public String getWeightRange() { return weightRange; }
    public void setWeightRange(String weightRange) { this.weightRange = weightRange; }
}